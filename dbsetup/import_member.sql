/* List data for all borrows associated with a barrower_id of 1.
SELECT *
FROM temp_raw_data
WHERE borrowerid = 1;
*/

/* List borrower names associated with more than one barrow.
SELECT borrowername
FROM temp_raw_data
GROUP BY borrowername
HAVING COUNT(*) > 1;
*/

/* It is clear that the IDs do not uniquely identify borrowers. These IDs will therefore be discarded. */

/* List borrower names which do not fit with a "Firstname Lastname" foramt.
SELECT bookID, borrowername
FROM temp_raw_data
WHERE borrowername !~ '^[A-Za-z]+ [A-Za-z]+$';
*/

/* Preview the removal of prefixes from borrower names which have them. 
SELECT
    bookID,
    borrowername,
    ARRAY_TO_STRING((STRING_TO_ARRAY(borrowername, ' '))[2:], ' ')
FROM temp_raw_data
WHERE borrowername ~ '^((Mr\.)|(Mrs\.)|(Dr\.)) [A-Za-z]+ [A-Za-z]+';
*/

/* Remove prefixes from borrower names who have them. */
UPDATE temp_raw_data
SET borrowername = ARRAY_TO_STRING((STRING_TO_ARRAY(borrowername, ' '))[2:], ' ')
WHERE borrowername ~ '^((Mr\.)|(Mrs\.)|(Dr\.)) [A-Za-z]+ [A-Za-z]+';

/* Preview removing suffixes from borrower names who still do not follow a "Firstname Lastname" format.
SELECT
    bookID,
    borrowername,
    ARRAY_TO_STRING((STRING_TO_ARRAY(borrowername, ' '))[1:2], ' ')
FROM temp_raw_data
WHERE borrowername !~ '^[A-Za-z]+ [A-Za-z]+$';
*/

/* Remove suffixes from borrower names who have them. */
UPDATE temp_raw_data
SET borrowername = ARRAY_TO_STRING((STRING_TO_ARRAY(borrowername, ' '))[1:2], ' ')
WHERE borrowername !~ '^[A-Za-z]+ [A-Za-z]+$';

/* Confirm that all names now follow a "Firstname Lastname" format.
SELECT COUNT(*)
FROM temp_raw_data
WHERE borrowername !~ '^[A-Za-z]+ [A-Za-z]+$';
*/

/*Preview extracting first and last names from borrowername column. 
SELECT DISTINCT 
