/* Create new members table. */
CREATE TABLE members (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    first_name text NOT NULL,
    last_name text NOT NULL
);

/* Import members into new tables. */
INSERT INTO members (first_name, last_name)
SELECT DISTINCT
    ARRAY_TO_STRING((STRING_TO_ARRAY(borrowername, ' '))[1:1], ' ') AS first,
    ARRAY_TO_STRING((STRING_TO_ARRAY(borrowername, ' '))[2:2], ' ') AS last
FROM temp_raw_data;